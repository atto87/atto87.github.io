Flower _entry(
  String id,
  String name,
  FlowerDifficulty difficulty,
  String season,
) {
  final details = _additionalDetails[id];
  return _flower(
    id: id,
    name: name,
    imagePath: 'assets/images/$id.jpg',
    difficulty: difficulty,
    season: season,
    colors: details?.colors ?? const ['準備中'],
    description:
        details?.description ?? '$nameは${difficulty.appLabel}として収録している花です。',
    howToIdentify: details?.howToIdentify ?? '$nameの見分け方は準備中です。',
    similarFlowers: details?.similarFlowers ?? const [],
  );
}

Flower _flower({
  required String id,
  required String name,
  required FlowerDifficulty difficulty,
  String? imagePath,
  String season = '準備中',
  List<String> colors = const ['準備中'],
  String? description,
  String? howToIdentify,
  List<String> similarFlowers = const [],
}) {
  return Flower(
    id: id,
    name: name,
    imagePaths: [
      imagePath ?? placeholderFlowerImagePath,
      'assets/images/${id}_context.jpg',
